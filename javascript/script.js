/*
Operadores Aritméticos
*/ 

//Multiplicação *
console.log(5 * 3.4);

//Divisão /
console.log(12 / 2); 

// Soma +
console.log(34 + 87);

// Subtração - 
console.log(123-12);

// Resto da divisão %
console.log(23 % 2);

// Incremento ++
let value = 9
console.log(++value);
console.log(value++);
console.log(value);

// ++ antes ele incremente o valor da variável
// instantaneamente no mesmo momento de
// ++ depois, ele só ira incrementar após a linha atual do código
// assim, só na próxima linha o veremos incrementado

// Decremento --
// Segue a mesma lógica acima
console.log(--value);
console.log(value--);
console.log(value);


// exponencial **
console.log(value ** 1);
console.log(value ** 9);
console.log(value ** 0);






