// Variabeis
/* Recebem valores  que podem ser diversos tipos de dados
Atalho de codigos
Identificadores
3 palavras reservadas para cirar uma variável:
1. var 
2. let 
3. const
*/

// Variáveis que podem alterar seu valor 
var clima = "Quente" // Declaração de variáevel
clima = "Frio"

let umidade = "Úmido" //Declaração de variável
umidade = "seco"


// Variáveis que permanecem constantes, sem mudar:
const sorvete = "Morango"

// resultará em erro caso faça isso;
// const sorvete = "Nutella"

console.log(clima + "\n" + umidade + "\n" + sorvete);

