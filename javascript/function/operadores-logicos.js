// Operadores Lógicos (logical operators)

// 2 valores booleanos, verdadeiro ou falso

let verdade = true;
let mentira = false;

// AND &&
console.log(verdade && mentira); //mentira
//Só resultará em verdadeiro qando os 2 foram verdadeiros


// OR ||
console.log(verdade || mentira); //verdade
//Basta 1 ser verdadeiro para resultar em verdadeiro qando

//! Not
// nega o valor original
console.log(!verdade); //false